import Foundation

class Calculator {
    
    
    func add(_ x: Int, _ y: Int) -> Int {
        return x + y
    }
    
    func substract(_ x: Int, _ y: Int) -> Int {
        return x - y
    }
    
    func nultiple(_ x: Int, _ y: Int) -> Int {
        return x * y
    }
    
    func division(_ x: Int, _ y: Int) -> Int {
        return x / y
    }
}
